let value=10;

console.log("*******While*******");
//while loop
while(value>=0){
    console.log(value);
    value--;
}

value=10;

console.log("*******Do While*******");
//Here do statement will run atleast once.
do{
    console.log(value);
    value--;
}
while(value>10);
console.log(value);


console.log("*******For Loop*******");



//for loop
for(let x=0;x<11;x++){
    console.log(x);
}

